function enrichNullOniBoss(boss: BossDefinition): BossDefinition {
  return {
    ...boss,
    phases: boss.phases.map((phase) => ({
      ...phase,
      attacks: phase.attacks.map((attack) => ({
        ...attack,
        ...(nullOniDetails[attack.id] ?? {}),
        castLine: nullOniDetails[attack.id]?.castLine ?? `${attack.id.replaceAll('-', '.')}.cast()`,
        phaseRole: nullOniDetails[attack.id]?.phaseRole ?? 'boss pattern',
        movementTell: nullOniDetails[attack.id]?.movementTell ?? attack.tellText ?? 'Read the boss pose before moving.',
        bossMotion: nullOniDetails[attack.id]?.bossMotion ?? motionForBehavior(attack),
        counterStyle: nullOniDetails[attack.id]?.counterStyle ?? counterForBehavior(attack),
        comboRole: nullOniDetails[attack.id]?.comboRole ?? roleForBehavior(attack),
        counterWindowMs: nullOniDetails[attack.id]?.counterWindowMs ?? attack.recoverMs,
        arenaEffect: nullOniDetails[attack.id]?.arenaEffect ?? arenaEffectForBehavior(attack),
        dangerLayers: nullOniDetails[attack.id]?.dangerLayers ?? dangerLayersForBehavior(attack),
        counterRhythm: nullOniDetails[attack.id]?.counterRhythm ?? counterRhythmForBehavior(attack),
        commandHint: nullOniDetails[attack.id]?.commandHint ?? commandHintForAttack(attack),
        phaseShift: nullOniDetails[attack.id]?.phaseShift ?? phaseShiftForAttack(attack),
        ...(!nullOniDetails[attack.id] ? {
          castLine: `${attack.id.replaceAll('-', '.')}.cast()`,
          phaseRole: 'boss pattern',
          movementTell: attack.tellText ?? 'Read the boss pose before moving.',
          bossMotion: motionForBehavior(attack),
          counterStyle: counterForBehavior(attack),
          comboRole: roleForBehavior(attack),
          counterWindowMs: attack.recoverMs,
          arenaEffect: arenaEffectForBehavior(attack),
        } : {}),
      })),
    })),
  };
}

function motionForBehavior(attack: BossAttackDefinition): string {
  if (attack.behavior === 'lane-sweep') return 'axis read into a wall-to-wall sweep';
  if (attack.behavior === 'mask-ring') return 'lift, crouch, and ring impact recovery';
  if (attack.behavior === 'rift-chain') return 'delayed pillar chain carved along the player route';
  if (attack.behavior === 'mirror-step') return 'decoy fade into a body return';
  return `${attack.shape} tell into ${attack.recoverMs}ms recovery`;
}

function counterForBehavior(attack: BossAttackDefinition): string {
  if (attack.behavior === 'lane-sweep') return 'cross the clean axis once, then reset';
  if (attack.behavior === 'mask-ring') return 'use the safe center or step through the rim';
  if (attack.behavior === 'rift-chain') return 'keep moving until the final pulse clears';
  if (attack.behavior === 'mirror-step') return 'wait for the real body before punishing';
  return 'read the tell, step late, punish during recovery';
}

function arenaEffectForBehavior(attack: BossAttackDefinition): string {
  if (attack.behavior === 'tracking-palm') return 'Marked footprint, palm impact, then a smaller echo pulse.';
  if (attack.behavior === 'mask-ring') return 'A safe center opens while the rim and aftershock threaten the edge.';
  if (attack.behavior === 'lane-sweep') return 'A full-room axis lane divides the arena into one clean crossing route.';
  if (attack.behavior === 'rift-chain') return 'Delayed void pillars bloom in sequence along the recent movement path.';
  if (attack.behavior === 'mirror-step') return 'Mask shadows fake the first target before the real return strike.';
  if (attack.behavior === 'binary-cage') return 'Closing bracket lanes leave one diagonal escape before impact.';
  if (attack.behavior === 'void-cleave') return 'A long sword-line carves from boss stance to landing skid.';
  if (attack.behavior === 'null-crosscut') return 'Two crossing cuts remove greedy recovery routes.';
  if (attack.behavior === 'null-pursuit') return 'Predicted footprints detonate as chained dash stops.';
  return `${attack.shape} pressure pattern`;
}

function dangerLayersForBehavior(attack: BossAttackDefinition): string[] {
  if (attack.behavior === 'tracking-palm') return ['last-footprint marker', 'palm impact', attack.aftershock ? 'echo aftershock' : 'short recovery'];
  if (attack.behavior === 'mask-ring') return ['rising mask tell', 'unsafe rim', attack.aftershock ? 'delayed rim echo' : 'center reset'];
  if (attack.behavior === 'lane-sweep') return ['axis glyph read', 'wall-to-wall lane', 'short punish timer'];
  if (attack.behavior === 'rift-chain') return ['first rift seed', 'delayed pillar chain', 'no double-back zone'];
  if (attack.behavior === 'mirror-step') return ['decoy shadow', 'real body return', attack.id.includes('plus') ? 'offset twin mark' : 'late punish'];
  if (attack.behavior === 'binary-cage') return ['vertical close tag', 'horizontal close tag', 'single diagonal exit'];
  if (attack.behavior === 'void-cleave') return ['draw stance', 'vanish lane', 'landing skid punish'];
  if (attack.behavior === 'null-crosscut') return ['first shoulder turn', 'return shoulder turn', 'empty diagonal'];
  if (attack.behavior === 'null-pursuit') return ['predicted footprint', 'dash chain', attack.id.includes('plus') ? 'recursive return route' : 'final stop'];
  return ['windup tell', 'active hitbox', 'recovery punish'];
}

function counterRhythmForBehavior(attack: BossAttackDefinition): string {
  if (attack.behavior === 'tracking-palm') return 'side-step on the glow, wait one echo beat, slash once';
  if (attack.behavior === 'mask-ring') return 'commit before the flash, pause for the rim, punish after the drop';
  if (attack.behavior === 'lane-sweep') return 'read axis, cross once, do not drift back';
  if (attack.behavior === 'rift-chain') return 'move forward through each pop, turn only after the last pillar';
  if (attack.behavior === 'mirror-step') return 'wait through the fake, step the return, punish the body';
  if (attack.behavior === 'binary-cage') return 'leave diagonally early, then take the long whiff window';
  if (attack.behavior === 'void-cleave') return 'step sideways before vanish ends, chase the landing dust';
  if (attack.behavior === 'null-crosscut') return 'keep moving through both cuts, swing after the second flash';
  if (attack.behavior === 'null-pursuit') return 'change direction every footprint, punish the final stop';
  return 'read, punish';
}

function commandHintForAttack(attack: BossAttackDefinition): string {
  if (attack.threatLevel && attack.threatLevel >= 4) return 'try.catch steadies the vow window after this pattern.';
  if (attack.comboRole === 'trap') return 'Save step until the trap closes, then answer with X.';
  if (attack.comboRole === 'finisher') return 'A clean step opens a two-hit sword punish.';
  return 'Movement first, sword second.';
}

function phaseShiftForAttack(attack: BossAttackDefinition): string {
  if (attack.id.includes('plus')) return 'phase two variant: faster windup, tighter escape, shorter punish.';
  if (attack.threatLevel && attack.threatLevel >= 4) return 'chapter-one mastery check: punishes panic movement.';
  return 'phase one lesson: teaches the base read before chained variants.';
}

function roleForBehavior(attack: BossAttackDefinition): NonNullable<BossAttackDefinition['comboRole']> {
  if (attack.behavior === 'mask-ring' || attack.behavior === 'void-cleave') return 'finisher';
  if (attack.behavior === 'binary-cage' || attack.behavior === 'rift-chain' || attack.behavior === 'null-crosscut') return 'trap';
  if (attack.behavior === 'mirror-step' || attack.behavior === 'null-pursuit') return 'follow-up';
  return 'opener';
}

